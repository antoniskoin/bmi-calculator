    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (notesList.getSelectedValue() != null) { // Check if user selected an item
            Notes.deleteNote(notesList.getSelectedValue().toString(), username);
            model.removeAllElements();
            Notes.populateNotes(model, txtCount, username);
            txtCount.setText("Notes: " + model.size());
        } else {
            JOptionPane.showMessageDialog(this, "Please select a note to delete.", "INFORMATION",
                                          JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtNoteAreaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNoteAreaKeyPressed
        if (notesList.getSelectedValue() == null) {
            JOptionPane.showMessageDialog(this, "Please create a note or select an existing one to start typing!", "INFORMATION",
                                          JOptionPane.INFORMATION_MESSAGE);
            txtNoteArea.setText("");
        }
    }//GEN-LAST:event_txtNoteAreaKeyPressed


    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        String title = txtTitle.getText();
        if (!title.isEmpty() && title != null) {
            Notes newNote = new Notes(title);
            int result = newNote.createNote(model, username);
            if (result == 1) {
                model.removeAllElements();
                Notes.populateNotes(model, txtCount, username);
            } else if (result == 2) {
                JOptionPane.showMessageDialog(this, "Failed to create new note.", "ERROR", JOptionPane.ERROR);
            } else {
                JOptionPane.showMessageDialog(this, "Note with the same title already exists!", "INFORMATION",
                                              JOptionPane.INFORMATION_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Title can't be empty!", "INFORMATION",
                                          JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnNewActionPerformed

    private void notesListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_notesListValueChanged
        if (model.size() > 0) {
            if (notesList.getSelectedValue() != null) {
                if (!notesList.getSelectedValue().isEmpty()) {
                    Notes.loadNote(notesList.getSelectedValue(), txtNoteArea, username);
                    txtNoteTitle.setText(notesList.getSelectedValue());
                }
            }
        }
    }//GEN-LAST:event_notesListValueChanged

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Notes.saveNote(txtNoteTitle.getText(), txtNoteArea.getText(), username);
    }//GEN-LAST:event_btnSaveActionPerformed

